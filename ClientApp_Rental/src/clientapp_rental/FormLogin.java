    public void createConn() {
        if (clientSocket == null) {
            try {
                clientSocket = new Socket("localhost", 6000);
            } catch (IOException ex) {
                Logger.getLogger(FormLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

