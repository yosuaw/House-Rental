    private void btnLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogInActionPerformed
        try {
            if (txtUsername.getText().isEmpty() || txtPassword.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Username atau Password masih kosong. Isikan data dengan lengkap!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            } else {
                BufferedReader msgFromServer = new BufferedReader(new InputStreamReader(clientSocket.getInputStream()));
                DataOutputStream msgToServer = new DataOutputStream(clientSocket.getOutputStream());

                msgToServer.writeBytes("login;-" + txtUsername.getText() + ";-" + txtPassword.getText() + "\n");

                String hasil = msgFromServer.readLine();

                if (hasil.equals("true")) {
                    JOptionPane.showMessageDialog(this, "Login Successful!", "Successful", JOptionPane.INFORMATION_MESSAGE);
                    FormChat formChat = new FormChat(this.clientSocket, txtUsername.getText());
                    formChat.setVisible(true);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Your username or password is wrong", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLogInActionPerformed

