    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        try {
            String chatClient, chatNama;

            chatClient = txtPesan.getText();
            chatNama = txtNama.getText();

            if (chatClient.contains("cek tanggal:")) {
                String hasil[] = chatClient.split(":");
                sendChat("cek tanggal;-" + hasil[1] + ";-" + hasil[2]);
            } else if (chatClient.contains("reservasi:")) {
                String hasil[] = chatClient.split(":");
                sendChat("reservasi;-" + hasil[1] + ";-" + txtNama.getText() + ";-" + hasil[2]);
            } else if (chatClient.contains("lacak:")) {
                String hasil[] = chatClient.split(":");
                sendChat("lacak;-" + hasil[1] + ";-" + txtNama.getText());
            } else if (chatClient.equals("cek list rumah")) {
                sendChat("cek list rumah");
            } else {
                sendChat(chatNama + ":" + chatClient);
            }
            txtChat.append(chatNama + ": " + chatClient + "\n");
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSendActionPerformed

