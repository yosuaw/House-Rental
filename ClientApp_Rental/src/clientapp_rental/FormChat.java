    public void getMessage() {
        try {
            chatFromServer = new BufferedReader(new InputStreamReader(clientSocket.getInputStream()));
            String chatServer = chatFromServer.readLine();
            hasil = chatServer;
            String[] splitted = chatServer.split("&");
            if (chatServer.contains("%cekListRumah%")) {
                for (int i = 0; i < splitted.length - 1; i++) {
                    String[] listInfo = splitted[i].split(";");
                    txtChat.append(listInfo[0] + ", Alamat : " + listInfo[1] + ", Harga Per Hari : Rp." + listInfo[2] + "\n");
                }
            } else if (chatServer.contains("showChat&&")) {
                FormShowClient formShow = new FormShowClient(hasil);
                formShow.setVisible(true);
            } else {
                txtChat.append(chatServer + "\n");
            }

        } catch (IOException ex) {
            Logger.getLogger(FormChat.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void sendChat(String msg) {
        try {
            sendToServer = new DataOutputStream(clientSocket.getOutputStream());
            sendToServer.writeBytes(msg + "\n");
        } catch (IOException ex) {
            Logger.getLogger(FormChat.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

